    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
          
        try
        {

            Class. forName("org.apache.derby.jdbc.ClientDriver").newInstance();
            String NAME=jTextField1.getText();
            String BRANCH=jComboBox1.getSelectedItem().toString();
            String SEM=jComboBox2.getSelectedItem().toString();
            String CONTACTNO=jTextField2.getText().toString();
            String sql="insert into PIANK values(?,?,?,?)";
            connection=DriverManager.getConnection(connectionURL,"ayu","ayu");
            statement=connection.prepareStatement(sql);
            statement.setString(1,NAME);
            statement.setString(2,BRANCH);
            statement.setString(3,SEM);
            statement.setString(4,CONTACTNO);
            statement.executeUpdate();
            JOptionPane.showMessageDialog(null,"The data has been succesfully saved into the database");
            
    }
         catch(SQLException se)
{
    se.printStackTrace();
}
        catch(Exception e)
        {
            e.printStackTrace();
        }
        
        

    }//GEN-LAST:event_jButton1ActionPerformed

