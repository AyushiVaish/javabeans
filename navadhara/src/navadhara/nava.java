    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        try
        {

            Class. forName("org.apache.derby.jdbc.ClientDriver").newInstance();
            String PARTICIPANT=jComboBox1.getSelectedItem().toString();
            String COLLEGE=jTextField1.getText();
            int NOOFMEMBER=Integer.parseInt(jComboBox2.getSelectedItem().toString());
            String NAMEOFPROJECT=jTextField2.getText();
            String CONTACTNOOFDEAN=jTextField4.getText();
            String sql="insert into OTHERCOLLEGE values(?,?,?,?,?)";
            connection=DriverManager.getConnection(connectionURL,"ayu","ayu");
            statement=connection.prepareStatement(sql);
            statement.setString(1,PARTICIPANT);
            statement.setString(2,COLLEGE);
            statement.setInt(3,NOOFMEMBER);
            statement.setString(4,NAMEOFPROJECT);
            statement.setString(5,CONTACTNOOFDEAN);
            statement.executeUpdate();
            JOptionPane.showMessageDialog(null,"The data has been succesfully saved into the database");
            
    }
         catch(SQLException se)
{
    se.printStackTrace();
}
        catch(Exception e)
        {
            e.printStackTrace();
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

