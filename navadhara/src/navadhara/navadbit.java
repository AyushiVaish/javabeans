    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        try
        {

            Class. forName("org.apache.derby.jdbc.ClientDriver").newInstance();
            String participant=jComboBox1.getSelectedItem().toString();
            int noofmember=Integer.parseInt(jComboBox2.getSelectedItem().toString());
            String nameofproject=jTextField1.getText();
            String sql="insert into navadhara values(?,?,?)";
            connection=DriverManager.getConnection(connectionURL,"ayu","ayu");
            statement=connection.prepareStatement(sql);
            statement.setString(1,participant);
            statement.setInt(2,noofmember);
            statement.setString(3,nameofproject);
            statement.executeUpdate();
            JOptionPane.showMessageDialog(null,"The data has been succesfully saved into the database");
            
    }
         catch(SQLException se)
{
    se.printStackTrace();
}
        catch(Exception e)
        {
            e.printStackTrace();
        }
        
        

    }//GEN-LAST:event_jButton1ActionPerformed

